static const char* ww_getFilename(WINFO* wp) {
    FTABLE* fp = wp->fp;
    return fp->fname;
}

static WINFO* ww_openFile(const char* pszFilename, int nOptions) {
    FTABLE* fp = ft_openFileWithoutFileselector(pszFilename, 0, &(FT_OPEN_OPTIONS) {.fo_isNewFile = 0});
    if (!fp) {
        return 0;
    }
    return WIPOI(fp);
}

